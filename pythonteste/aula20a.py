def linha():            # definição da função (sem parâmetros)
    print("-" * 30)     # comandos da função


linha()                 # chamada da função
print("CURSO EM VÍDEO")
linha()
print("APRENDA PYTHON")
linha()
print("GUSTAVO GUANABARA")
linha()


def mensagem(msg):      # definição da função (com parâmetro)
    linha()             # comandos da função
    print(msg)
    linha()


mensagem("CURSO EM VÍDEO")      # chamada da função
mensagem("APRENDA PYTHON")
mensagem("GUSTAVOO GUANABARA")
